//Comparison Operator --> usesd to compare the relation between two numbers

console.log("Greater than > : ", 2>3);

console.log("Less than < : ", 4<2);

console.log("Greater than or Equal to  >= : ", 4>=3);

console.log("Less than  or Equal to <= : ", 5<=4);

console.log("Equal to : ",5===5);

console.log("Not Equal to  : ", 4!==5);

// Equality Operator --> used to find the given value is equal or not
// Two types .They are:
// 1 Loose Equality
// 2 strict Equality 
let num = 1
let str = '1'
console.log("Loose Equality : ", num==str)
console.log("Strict Equality: ", num===str)
